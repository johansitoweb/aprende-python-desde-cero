#Las variables son contenedores o cajas que almacenan datos. En Python, no es necesario declarar el tipo de variable
nombre = "Juan"      # Cadena de texto
edad = 25            # Entero
altura = 1.75        # Flotante
es_estudiante = True  # Booleano

print(f"Nombre: {nombre}, Edad: {edad}, Altura: {altura}, Estudiante: {es_estudiante}")
#Tipos de datos comunes:

#Cadenas (str): Texto.
# Enteros (int): Números enteros.
# Flotantes (float): Números con decimales.
# Booleanos (bool): Verdadero o falso.

